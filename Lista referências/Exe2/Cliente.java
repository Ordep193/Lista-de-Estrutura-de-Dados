package Exercicio.Exe2;

public class Cliente {
    private int cod;
    private String nome;
    private float tele;

    public void setCod(int cod){
        this.cod = cod;
    }
    public int getCod(){
        return this.cod;
    }
    public void setNome(String nome){
        this.nome = nome;
    }
    public String getNome(){
        return this.nome;
    }
    public void setTel(float tele){
        this.tele = tele;
    }
    public float getTel(){
        return this.tele;
    }

}
